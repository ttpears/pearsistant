// This bot is intended to bridge some 
// functionality between Home Assistant and
// Discord.
// In particular, exposing webhooks and a spotify 
// bot to Guilds

const { REST, Routes, Discord } = require('discord.js');
const { token, clientId, tokenId, guildId } = require('./config.json');
const request = require('request');
const fs = require('node:fs');
const path = require('node:path');

// Load discord.js and friends
const { Client, Collection, GatewayIntentBits } = require('discord.js');

// Initialize the client
const client = new Client({
	intents: [
		GatewayIntentBits.Guilds,
		GatewayIntentBits.GuildMessages,
		GatewayIntentBits.MessageContent,
		GatewayIntentBits.GuildMembers,
	],
});

// Load commands
const commands = [];
const commandFiles = fs.readdirSync('./commands').filter(file => file.endsWith('.js'));

for (const file of commandFiles) {
	const command = require(`./commands/${file}`);
	commands.push(command.data.toJSON());
}

const rest = new REST({ version: '10' }).setToken(token);

(async () => {
	try {
		console.log(`Started refreshing ${commands.length} application (/) commands.`);

		const data = await rest.put(
			Routes.applicationGuildCommands(clientId, guildId),
			{ body: commands },
		);

		console.log(`Successfully reloaded ${data.length} application (/) commands.`);
	} catch (error) {
		console.error(error);
	}
})();

// Load events
const eventsPath = path.join(__dirname, 'events');
const eventFiles = fs.readdirSync(eventsPath).filter(file => file.endsWith('.js'));

for (const file of eventFiles) {
	const filePath = path.join(eventsPath, file);
	const event = require(filePath);
	if (event.once) {
		client.once(event.name, (...args) => event.execute(...args));
	} else {
		client.on(event.name, (...args) => event.execute(...args));
	}
}

// Process commands
client.on('interactionCreate', async interaction => {
	if (!interaction.isChatInputCommand()) return;

	const runCommand = client.commands.get(interaction.commandName);

	if (!runCommand) return;

	try {
		await runCommand.execute(interaction);
	} catch (error) {
		console.error(error);
		await interaction.reply({ content: 'There was an error while executing this command!', ephemeral: true });
	}
});

client.login(token);
